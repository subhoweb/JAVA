public class wrapper {
	public static void main(String[] args) {
		    Integer myInt = 5; //Wrapper Objects
		    Double myDouble = 5.99; //Wrapper Objects
		    Character myChar = 'A'; //Wrapper Objects
		    
		    System.out.println(myInt);
		    System.out.println(myDouble.doubleValue()); //Wrapper Method
		    System.out.println(myChar);
		    
		    Integer num = 10000;
		    String svar = num.toString(); //String Conversion
		    System.out.println(svar.length()); // Length Calculation

	}

}
